package Operadores;

public class OperadoresLogicos {

    public static void main(String[] args) {

        //Operadores

        /*
        && - Operadoe "e" e declarado com o e comercial
        || - Operador "ou" declaracao  "afunda ou nao afunda" pega a parte que esta certa
        ! - Operador NOT e declarado com !

         */


        System.out.println(true&&false);
        System.out.println(false||true);
        System.out.println(!false + " not");
    }
}
